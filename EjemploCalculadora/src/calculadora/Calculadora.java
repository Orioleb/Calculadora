package calculadora;

public class Calculadora {
	
	/*
	 * Metodo que devuelve la suma de dos valores pasados por parametro
	 * @param a
	 * @param b
	 */
	public double suma(double a , double b) {
		return (a+b);
	}

	/*
	 * Metodo que devuelve la resta de dos valores pasados por parametro
	 * @param a
	 * @param b
	 */
	public double resta(double a, double b) {
		return (a-b);
	}
	
	/*
	 * Metodo que devuelve la multiplicacion de dos valores pasados por parametro
	 * @param a
	 * @param b
	 */
	public double multiplica(double a, double b) {
		return (a*b);
	}
	
	/*
	 * Metodo que devuelve la division de dos valores pasados por parametro
	 * @param a
	 * @param b
	 */
	public double divide(double a, double b) {
		return (a/b);
	}
}
